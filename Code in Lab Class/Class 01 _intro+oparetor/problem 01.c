#include<stdio.h>
int main()
{
    int num=100;
    float f=10.55;
    double d=27382.34353;
    char ch='A';

    printf("The integer value is: %d\n",num);
    printf("The float value is: %f\n",f);
    printf("The double value is: %lf\n",d);
    printf("The character value is: %c\n",ch);
    return 0;
}
